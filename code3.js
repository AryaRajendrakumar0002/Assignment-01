//to print 0 t0 15 and checks even or odd number 
for (let i=0; i<=15; i++) {
    if (i == 0) {
            console.log(i +  " is an even number");
    }
    else if (i % 2 == 0) {
            console.log(i + " is an even number");   
    }
    else {
            console.log(i + " is an odd number");
    }
}